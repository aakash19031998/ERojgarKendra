package com.twocoms.rojgarkendra.successstoryscreen.model;

public class TestimonialsModel {

    public int postedBy = 0;
    public String postedOn = "";
    public String userName = "";
    public String userEmail = "";
    public String userContact = "";
    public String profileUrl = "";
    public String postedMessage = "";


    public int getPostedBy() {
        return postedBy;
    }

    public void setPostedBy(int postedBy) {
        this.postedBy = postedBy;
    }

    public String getPostedOn() {
        return postedOn;
    }

    public void setPostedOn(String postedOn) {
        this.postedOn = postedOn;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getUserEmail() {
        return userEmail;
    }

    public void setUserEmail(String userEmail) {
        this.userEmail = userEmail;
    }

    public String getUserContact() {
        return userContact;
    }

    public void setUserContact(String userContact) {
        this.userContact = userContact;
    }

    public String getProfileUrl() {
        return profileUrl;
    }

    public void setProfileUrl(String profileUrl) {
        this.profileUrl = profileUrl;
    }


    public String getPostedMessage() {
        return postedMessage;
    }

    public void setPostedMessage(String postedMessage) {
        this.postedMessage = postedMessage;
    }



}
